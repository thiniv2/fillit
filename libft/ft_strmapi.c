#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	size_t	len;
	char	*str;

	i = 0;
	if (!(s))
		return (NULL);
	len = ft_strlen((char*)s);
	str = (char*)malloc(sizeof(char) * len + 1);
	if (!(s) || !(str))
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
